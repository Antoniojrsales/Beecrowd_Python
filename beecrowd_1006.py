nota_um = float(input('Digite a primeira nota: '))
nota_dois = float(input('Digite a segunda nota: '))
nota_tres = float(input('Digite a terceira nota: '))
resultado = ((nota_um * 2) + (nota_dois * 3) + (nota_tres * 5)) / 10
print(f'A primeira nota {nota_um} + a nota dois {nota_um} + a nota tres {nota_tres} tem a media de {resultado:.1f}')
